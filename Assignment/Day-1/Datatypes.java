        System.out.println("Printing Data type int: " + num2);
        System.out.println("Printing Data type long: " + num3);
        System.out.println("Printing Data type float: " + num4);
        System.out.println("Printing Data type double: " + num5);
        System.out.println("Printing Data type boolean: " + bol);
